			Latvijas skolēnu mācību uzņēmums Light Up tika izveidots 2016. gadā JA Latvija SMU programmas ietvaros, sadarbojoties Jelgavas Spīdolas ģimnāzijas 12. klases skolniekiem Tomam Sutrim, Elgaram Rihardam un Ralfam Rogaļevam. Skolotāju Intas Jorniņas un Ivara Bahmaņa atbalstīti, puiši nodibināja SMU, kas izgatavo inovatīvas moduļveida kuba lampas, kuru apgaismojumu iespējams vadīt attālināti ar viedtālruni.
			</p>
			<p>
			LIAA lepojas ar SMU Light Up sasniegumiem, demonstrējot savu sniegumu tik sīvā konkurencē. LIAA projektu vadītāja Dace Dumbere-Bregže apliecina: “Bija patiess prieks redzēt jauniešu panākumus, klātienē vērojot, ka komandas produkts un prezentācija raisīja plašu interesi mediju vidū un piesaistīja dažādu uzņēmējdarbības veicināšanas organizāciju pārstāvju uzmanību. LIAA cer, ka Light Up jaunieši turpinās strādāt pie sava produkta attīstības un drīzumā tas būs pieejams arī plašākam patērētāju lokam”.
			</p>
			<p>
			28. starptautiskais SMU Eiropas fināls no 11. līdz 13. jūlijam norisinājās Beļģijas galvaspilsētā Briselē, un tajā piedalījās 37 valstu labākie SMU. Titulu Labākais gada Eiropas SMU saņem skolēnu uzņēmumi, kas demonstrējuši labākos rezultātus, prasmes un iemaņas komunikācijā, komandas darbā, problēmu risināšanā, mērķu izvirzīšanā, plānošanā un analīzē, finanšu administrēšanā, produktu attīstīšanā, mārketingā un pārdošanā un kam kopumā ir labākie finanšu rezultāti.
