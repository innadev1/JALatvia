			Latvijas jauniešu dalība Eiropas SMU fināla konkursā tiek īstenota LIAA projekta Inovāciju motivācijas programma ietvaros, ko līdzfinansē Eiropas Reģionālās attīstības fonds un Eiropas Savienība, sadarbībā ar ilggadējo partneri Swedbank un RTU Biznesa skolu.

			Jaunieši pateicas par finansiālo atbalstu SIA "Evopipes", Jelgavas pilsētas domei, veikalam "Kanclera nams", konkursam "Biznesa Ekspresis", kā arī citiem atbalstītājiem - Jelgavas Tehnoloģiju vidusskolai, Agate Hotel, LU Biznesa inkubatoram.

